int	ft_atoi(char *str)
{
	int	s;
	int	r;

	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	s = 1;
	if (*str == '-')
	{
		s = s * -1;
		str++;
	}
	else if (*str == '+')
		str++;
	r = 0;
	while (*str >= '0' && *str <= '9')
	{
		r = (r * 10) + (*str - 48);
		str++;
	}
	return (r * s);
}
